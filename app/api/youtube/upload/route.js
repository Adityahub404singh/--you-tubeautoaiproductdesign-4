import { NextResponse } from "next/server";
import { cookies } from "next/headers";
import { getValidAccessToken } from "@/lib/youtube-token";
import { readFile } from "fs/promises";
import { existsSync } from "fs";
import path from "path";

export async function POST(request) {
  try {
    const cookieStore = await cookies();
    let accessToken;
    try {
      const result = await getValidAccessToken(cookieStore);
      accessToken = result.accessToken;
    } catch (err) {
      if (err.message === "NOT_CONNECTED") return NextResponse.json({ error: "YouTube connected nahi hai" }, { status: 401 });
      throw err;
    }

    const { videoUrl, title, description, tags, categoryId, privacyStatus, scheduledTime, language, videoType } = await request.json();
    if (!videoUrl || !title) return NextResponse.json({ error: "videoUrl aur title required" }, { status: 400 });

    const isShorts = videoType === "shorts" || title?.toLowerCase().includes("#shorts");

    // Local file read
    const localPath = videoUrl.replace("http://localhost:3000", "").replace("/storage/", "");
    const filePath = path.join(process.cwd(), "storage", localPath);
    console.log("Video file:", filePath, "exists:", existsSync(filePath));

    if (!existsSync(filePath)) {
      return NextResponse.json({ error: "Video file not found: " + filePath }, { status: 404 });
    }

    const buffer = await readFile(filePath);
    const videoBytes = new Uint8Array(buffer);
    const totalSize = videoBytes.length;
    console.log("✅ Video file read! Size:", (totalSize/1024/1024).toFixed(2) + "MB");

    // Shorts ke liye title mein #Shorts add karo
    const finalTitle = isShorts && !title.includes("#Shorts") 
      ? title.slice(0, 90) + " #Shorts"
      : title.slice(0, 100);

    const finalDesc = (description || "AI Generated by YouTubeAuto.ai") + 
      "\n\n" + (isShorts ? "#Shorts #Short #viral #trending #hindi\n" : "") +
      "#viral #trending #hindi #india #youtube";

    const videoMetadata = {
      snippet: {
        title: finalTitle,
        description: finalDesc,
        tags: [...(tags?.slice(0, 13) || []), ...(isShorts ? ["Shorts", "Short"] : [])],
        categoryId: categoryId || "22",
        defaultLanguage: language || "hi"
      },
      status: {
        privacyStatus: scheduledTime ? "private" : (privacyStatus || "private"),
        selfDeclaredMadeForKids: false,
        ...(scheduledTime ? { publishAt: new Date(scheduledTime).toISOString() } : {})
      }
    };

    // Init resumable upload
    const initResponse = await fetch(
      "https://www.googleapis.com/upload/youtube/v3/videos?uploadType=resumable&part=snippet,status",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${accessToken}`,
          "Content-Type": "application/json",
          "X-Upload-Content-Type": "video/mp4",
          "X-Upload-Content-Length": totalSize.toString()
        },
        body: JSON.stringify(videoMetadata),
        signal: AbortSignal.timeout(30000)
      }
    );

    if (!initResponse.ok) {
      const e = await initResponse.json();
      console.error("Init failed:", JSON.stringify(e));
      throw new Error(JSON.stringify(e));
    }

    const uploadUrl = initResponse.headers.get("Location");
    if (!uploadUrl) throw new Error("Upload URL missing");
    console.log("Upload URL: ✅ Got it");

    // Chunked upload - 2MB chunks for reliability
    const CHUNK_SIZE = 2 * 1024 * 1024; // 2MB
    let uploaded = 0;
    let attempts = 0;

    while (uploaded < totalSize) {
      const end = Math.min(uploaded + CHUNK_SIZE, totalSize);
      const chunk = videoBytes.slice(uploaded, end);
      console.log(`Uploading chunk ${uploaded}-${end-1}/${totalSize} (${((uploaded/totalSize)*100).toFixed(1)}%)`);

      try {
        const chunkResponse = await fetch(uploadUrl, {
          method: "PUT",
          headers: {
            "Content-Type": "video/mp4",
            "Content-Length": chunk.length.toString(),
            "Content-Range": `bytes ${uploaded}-${end-1}/${totalSize}`
          },
          body: chunk,
          signal: AbortSignal.timeout(180000) // 3 min per chunk
        });

        if (chunkResponse.status === 308) {
          // Continue uploading
          const range = chunkResponse.headers.get("Range");
          uploaded = range ? parseInt(range.split("-")[1]) + 1 : end;
          attempts = 0;
          continue;
        }

        if (chunkResponse.status === 200 || chunkResponse.status === 201) {
          const result = await chunkResponse.json();
          console.log("✅ YouTube upload success! Video ID:", result.id);
          return NextResponse.json({
            success: true,
            videoId: result.id,
            youtubeUrl: `https://www.youtube.com/watch?v=${result.id}`,
            isShorts,
            status: result.status?.privacyStatus
          });
        }

        const errText = await chunkResponse.text();
        throw new Error(`Chunk failed: ${chunkResponse.status} - ${errText.slice(0, 200)}`);

      } catch (chunkErr) {
        attempts++;
        console.log(`Chunk error (attempt ${attempts}):`, chunkErr.message);
        if (attempts >= 3) throw new Error("Max retry attempts reached: " + chunkErr.message);
        await new Promise(r => setTimeout(r, 2000 * attempts)); // backoff
      }
    }

    throw new Error("Upload loop completed without success");
  } catch (error) {
    console.error("YouTube upload error:", error.message);
    return NextResponse.json({ error: error.message }, { status: 500 });
  }
}
